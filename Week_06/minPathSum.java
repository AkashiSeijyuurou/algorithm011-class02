class Solution {
    public int minPathSum(int[][] grid) {
        //1.å­é—®é¢˜ gridçš„æœ€å°è·¯å¾„å’Œæ˜¯å³è¾¹æ ¼å­å’Œä¸‹è¾¹æ ¼å­çš„æœ€å°è·¯å¾„å’Œä¹‹é—´çš„æœ€å°å€¼
        //2.çŠ¶æ€æ•°ç»„ grid[i][j]  ä»Žç»ˆç‚¹éåŽ†ï¼Œä»¥ï¼ˆi, jï¼‰ä¸ºèµ·ç‚¹åˆ°ç»ˆç‚¹çš„æœ€å°è·¯å¾„å’Œ
        //3.dpæ–¹ç¨‹ grid[i][j] += min(grid[i + 1][j], grid[i][j]);
        //time O(mn)
        //space O(1)
        
        int m = grid.length;
        if (m == 0) {
            return 0;
        }

        int n = grid[0].length;

        for (int i = m - 2; i >= 0; i--) {
            grid[i][n - 1] += grid[i + 1][n - 1];
        }

        for (int i = n - 2; i >= 0; i--) {
            grid[m - 1][i] += grid[m - 1][i + 1];
        }

        for (int i = m - 2; i >= 0; i--) {
            for (int j = n - 2; j >= 0; j--) {
                grid[i][j] += Math.min(grid[i + 1][j], grid[i][j + 1]);
            }
        }

        return grid[0][0];
    }
}
